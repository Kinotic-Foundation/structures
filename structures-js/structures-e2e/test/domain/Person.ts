import {AutoGeneratedId, Entity, MultiTenancyType, Precision, PrecisionType} from '@kinotic/structures-api'
import {Address} from './Address.js'

@Entity(MultiTenancyType.SHARED)
export class Person {
    @AutoGeneratedId
    public id!: string
    public firstName: string = ''

    public lastName: string = ''

    @Precision(PrecisionType.SHORT)
    public age?: number

    public address!: Address
}
